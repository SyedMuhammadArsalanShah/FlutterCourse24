  // For Loop
  for (var i = 1; i <= 10; i++) {
    print("Value Of ${i}");
  }

  // While Loop

  var i = 0;
  while (i <= 10) {
    print("Value Of ${i} using while loop");
    i++;
  }

  int j = 11;

  do {
    print("Value Of ${j} using  do while loop");
    j++;
  } while (j <= 10);



  


